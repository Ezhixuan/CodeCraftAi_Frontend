const handleDownloadClick = async () => {
  if (!appId.value) {
    message.error('Â∫îÁî®ID‰∏çÂ≠òÂú®')
    return
  }

  try {
    downloadLoading.value = true
    const url = `${getBaseUrl()}/app/download/${appId.value}`
    const response = await fetch(url, {
      method: 'GET',
      credentials: 'include',
    })

    if (!response.ok) {
      throw new Error(`‰∏ãËΩΩÂ§±Ë¥•: ${response.status}`)
    }

    // È™åËØÅ Content-Type
    const contentType = response.headers.get('content-type') || ''
    if (!contentType.includes('application/zip')) {
      throw new Error('‰∏çÊîØÊåÅÁöÑÊñá‰ª∂Á±ªÂûã')
    }

    // Ëé∑ÂèñÊñá‰ª∂Âêç
    const contentDisposition = response.headers.get('Content-Disposition')
    const fileName =
      contentDisposition?.match(/filename="(.+)"/)?.[1] ||
      `${appInfo.value?.name || 'app'}_code.zip`

    // Ëé∑ÂèñÂéüÂßã‰∫åËøõÂà∂Êï∞ÊçÆ
    const blob = await response.blob()

    // È™åËØÅZIPÊñá‰ª∂Â§¥
    const arrayBuffer = await blob.arrayBuffer()
    const view = new DataView(arrayBuffer)
    const zipHeader = (view.getUint8(0) << 8) + view.getUint8(1)
    if (zipHeader !== 0x504b) {
      throw new Error('‰∏çÊòØÊúâÊïàÁöÑZIPÊñá‰ª∂')
    }

    // ÂàõÂª∫‰∏ãËΩΩÈìæÊé•
    const downloadUrl = URL.createObjectURL(blob)
    const link = document.createElement('a')
    link.href = downloadUrl
    link.download = fileName
    document.body.appendChild(link)
    link.click()
    document.body.removeChild(link)
    URL.revokeObjectURL(downloadUrl)

    message.success('‰ª£Á†Å‰∏ãËΩΩÊàêÂäü')
  } catch (error) {
    console.error('‰∏ãËΩΩÂ§±Ë¥•:', error)
    message.error('‰∏ãËΩΩÂ§±Ë¥•ÔºåËØ∑Á®çÂêéÈáçËØï')
  } finally {
    downloadLoading.value = false
  }
}

